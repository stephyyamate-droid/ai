const generateBtn =
document.getElementById("generate");

const promptInput =
document.getElementById("prompt");

const messages =
document.getElementById("messages");

const code =
document.getElementById("code");

const preview =
document.getElementById("preview");

const downloadBtn =
document.getElementById("download");



const API_URL =
"https://veltrix-ai-api.stephyyamate.workers.dev";



let files = {

    "index.html": `
<h1>Welcome to V Veltrix.ai</h1>
<p>Your AI website will appear here.</p>
`,

    "style.css": `
body{
font-family:Arial;
}
`,

    "app.js": `
console.log("Generated by Veltrix.ai");
`

};



let currentFile = "index.html";





function addMessage(text,type){

    const div =
    document.createElement("div");


    div.className =
    "message " + type;


    div.innerHTML =
    text;


    messages.appendChild(div);


    messages.scrollTop =
    messages.scrollHeight;

}






function updateEditor(){


    code.textContent =
    files[currentFile];


}






function updatePreview(){


    preview.srcdoc =

    files["index.html"]

    +

    `
    <style>
    ${files["style.css"]}
    </style>
    `

    +

    `
    <script>
    ${files["app.js"]}
    <\/script>
    `;


}








async function generate(){


let prompt =
promptInput.value.trim();



if(!prompt)
return;



addMessage(
"👤 " + prompt,
"user"
);



addMessage(
"⚡ V Veltrix.ai is building...",
"ai"
);




try {



const response =
await fetch(
API_URL,
{

method:"POST",

headers:{

"Content-Type":
"application/json"

},


body:
JSON.stringify({

prompt:prompt

})

}

);





const data =
await response.json();





let text =
data.output[0]
.content[0]
.text;





let project =
JSON.parse(text);





files =
project.files;





addMessage(
"✅ Website generated!",
"ai"
);



currentFile =
"index.html";


updateEditor();


updatePreview();





}

catch(error){


console.error(error);


addMessage(
"❌ Error connecting to V Veltrix.ai",
"ai"
);


}



}







generateBtn.onclick =
generate;








// FILE SWITCHING


document
.querySelectorAll(".file")
.forEach(button=>{


button.onclick = ()=>{


document
.querySelectorAll(".file")
.forEach(b=>
b.classList.remove("active")
);



button.classList.add("active");



currentFile =
button.dataset.file;



updateEditor();



};



});









// ZIP DOWNLOAD


downloadBtn.onclick =
async()=>{


const zip =
new JSZip();



zip.file(
"index.html",
files["index.html"]
);



zip.file(
"style.css",
files["style.css"]
);



zip.file(
"app.js",
files["app.js"]
);



const blob =
await zip.generateAsync(
{
type:"blob"
}
);



const link =
document.createElement("a");


link.href =
URL.createObjectURL(blob);



link.download =
"Veltrix-Website.zip";


link.click();



};







updateEditor();

updatePreview();
